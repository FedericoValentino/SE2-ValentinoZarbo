package it.polimi.se2.codekata.GeneralStuff;

public class SourcesElement
{
    public int gID;
    public String Sources;

    public SourcesElement(int gID, String sources) {
        this.gID = gID;
        Sources = sources;
    }

    public int getgID() {
        return gID;
    }

    public String getSources() {
        return Sources;
    }
}
